package it.unibo.slam.utils;

/**
 * Class containing utility constants.
 */
public final class Constants
{
	/**
	 * Constant that expresses the number of bytes used in a double value.
	 */
	public static final int BYTES_PER_DOUBLE = Double.SIZE / Byte.SIZE;
	
	/**
	 * Constant that expresses the number of bytes used in a float value.
	 */
	public static final int BYTES_PER_FLOAT = Float.SIZE / Byte.SIZE;
	
	/**
	 * Constant that expresses the number of bytes used in an integer value.
	 */
	public static final int BYTES_PER_INTEGER = Integer.SIZE / Byte.SIZE;
	
	/**
	 * Constant that expresses the number of bytes used in a long value.
	 */
	public static final int BYTES_PER_LONG = Long.SIZE / Byte.SIZE;
	/**
	 * Constant that expresses the number of bytes used in an short value.
	 */
	public static final int BYTES_PER_SHORT = Short.SIZE / Byte.SIZE;
}
